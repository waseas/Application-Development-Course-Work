    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) custJTable.getModel();

        model.setRowCount(0);
        
            for (Customer customer : ecosystem.getCustomerDirectory().getCustomerList()) {
                Object[] row = new Object[5];
                row[0] = customer;
                row[1] = customer.getCustPhoneNumber();
                row[2] = customer.getCustAddress();
                row[3] = customer.getUserAccount().getUsername();

                model.addRow(row);
                }  
    }
