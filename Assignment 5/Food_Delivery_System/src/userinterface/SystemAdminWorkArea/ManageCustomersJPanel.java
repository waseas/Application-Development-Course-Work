    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        String username = txtCustUserName.getText();
        String password = String.valueOf(txtCustPass.getPassword());
        String name = txtCustName.getText();
        String address = txtCustAdd.getText();
        int phoneNumber = Integer.parseInt(txtCustPhoneNum.getText());        
        
        Employee employee = ecosystem.getEmployeeDirectory().createEmployee(name);
        
        UserAccount account = ecosystem.getUserAccountDirectory().createUserAccount(username, password, employee, new CustomerRole());
        
        Customer c = ecosystem.getCustomerDirectory().createCustomer(name, address, phoneNumber, account);      
                
        txtCustName.setText("");
        txtCustPhoneNum.setText("");
        txtCustAdd.setText("");
        txtCustUserName.setText("");
        txtCustPass.setText("");
        populateTable();
        JOptionPane.showMessageDialog(null, "Customer Created");
        
    }//GEN-LAST:event_btnCreateActionPerformed

